
public class Switch_examples {

	public static void main(String[] args) {
		// Swtich examples
		// NOTE :- every case must finish with break ; stament
		
		String car="merc23";
		
		switch (car) {
		case "BMW":
			System.out.println("I have BMW car");
			break;
			
		case "Limo":
			System.out.println("I have BMW Limo");
			break;
			
		case "merc":
			System.out.println("I have BMW merc");
			break;
		case "ferrari":
			System.out.println("I have BMW ferrari");
			break;

		default:
			System.out.println("please neter corrrect car name");
			break;
		}
		
		
		
		
		

	}

}
